/**
 * A syntactically-valid NKey user-identifier: `U` + 55 base32 chars
 * (RFC-4648 alphabet `A-Z2-7`). Real keys are generated by the `nkeys` lib;
 * tests just need the regex to pass.
 */
const VALID_NKEY = "U" + "ABCDEFGHIJKLMNOPQRSTUVWXYZ234567ABCDEFGHIJKLMNOPQRSTUVZ"; // 1 + 55 = 56

function minOperator() {
  return { id: "andreas" };
}

function minDiscordPresence() {
  return {
    token: "discord-bot-token",
    guildId: "1487000000000000000",
    agentChannelId: "1487000000000000001",
    logChannelId: "1487000000000000002",
  };
}

function minAgent(overrides: Record<string, unknown> = {}) {
  return {
    id: "luna",
    displayName: "Luna",
    persona: "./personas/luna.md",
    presence: { discord: minDiscordPresence() },
    ...overrides,
  };
}

function minConfig() {
  return {
    principal: minOperator(),
    agents: [minAgent()],
    claude: {},
  };
}

