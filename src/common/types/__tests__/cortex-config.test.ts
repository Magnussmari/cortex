describe("NatsConfigSchema", () => {
  test("rejects http:// url", () => {
    expect(() => NatsConfigSchema.parse({ url: "http://localhost:4222" })).toThrow();
  });

  test("accepts nats:// url with defaults", () => {
    const parsed = NatsConfigSchema.parse({ url: "nats://localhost:4222" });
    expect(parsed.name).toBe("cortex");
    expect(parsed.subjects).toEqual([]);
  });

  test("accepts tls:// url", () => {
    const parsed = NatsConfigSchema.parse({ url: "tls://nats.example.com:4222" });
    expect(parsed.url).toBe("tls://nats.example.com:4222");
  });

  test("NatsIdentity rejects non-NKey publicKey", () => {
    expect(() => NatsIdentitySchema.parse({
      seedPath: "/etc/cortex/operator.nk",
      publicKey: "not-an-nkey",
    })).toThrow();
  });

  test("NatsIdentity rejects short NKey-shaped strings (Holly review)", () => {
    // Real NATS NKey user keys are exactly 56 chars (U + 55 base32). Short
    // placeholders like "U1" or "UABCDEF1234567890" must be rejected so a
    // typo at config time fails fast, not silently at NatsLink.connect.
    expect(() => NatsIdentitySchema.parse({
      seedPath: "/etc/cortex/operator.nk",
      publicKey: "U1",
    })).toThrow();
    expect(() => NatsIdentitySchema.parse({
      seedPath: "/etc/cortex/operator.nk",
      publicKey: "UABCDEF1234567890",
    })).toThrow();
  });

  test("NatsIdentity rejects lowercase / base32-incompatible chars", () => {
    // NKey base32 alphabet is A-Z 2-7 (RFC-4648). Lowercase, 0, 1, 8, 9 are
    // not in the alphabet; only the U prefix + 55 valid chars pass.
    const valid55 = "ABCDEFGHIJKLMNOPQRSTUVWXYZ234567" + "ABCDEFGHIJKLMNOPQRSTUV"; // 32 + 22 = 54... need 55
    const full = "U" + (valid55 + "Z");
    expect(full).toHaveLength(56);
    const parsed = NatsIdentitySchema.parse({
      seedPath: "/etc/cortex/operator.nk",
      publicKey: full,
    });
    expect(parsed.publicKey).toBe(full);

    expect(() => NatsIdentitySchema.parse({
      seedPath: "/etc/cortex/operator.nk",
      publicKey: "U" + "abcdefghijklmnopqrstuvwxyz".padEnd(55, "A"),
    })).toThrow();
  });

  // C-108 (Prereq B for cortex#67): the account signing key path field is
  // optional, accepts a string path, and parses through unchanged. We don't
  // validate the path's contents at schema time — that's the loader's job
  // (`src/common/config/account-signing-key.ts`). Schema only carries the
  // shape; runtime owns the chmod + prefix gates.

  test("accountSigningKeyPath is optional (absent → undefined)", () => {
    const parsed = NatsConfigSchema.parse({ url: "nats://localhost:4222" });
    expect(parsed.accountSigningKeyPath).toBeUndefined();
  });

  test("accountSigningKeyPath accepts a string path", () => {
    const parsed = NatsConfigSchema.parse({
      url: "nats://localhost:4222",
      accountSigningKeyPath: "/etc/cortex/account-signing.nk",
    });
    expect(parsed.accountSigningKeyPath).toBe("/etc/cortex/account-signing.nk");
  });

  test("accountSigningKeyPath rejects non-string", () => {
    expect(() => NatsConfigSchema.parse({
      url: "nats://localhost:4222",
      accountSigningKeyPath: 42,
    })).toThrow();
  });

  // ─── credsPath (cortex#86) ─────────────────────────────────────────────
  // Path is just a string at the schema layer; chmod 600 + ~/ expansion +
  // file-read live in the NatsLink loader (`../bus/nats/connection.ts`).
  // Schema only carries shape so legacy + cortex config loaders agree.

  test("credsPath is optional (absent → undefined)", () => {
    const parsed = NatsConfigSchema.parse({ url: "nats://localhost:4222" });
    expect(parsed.credsPath).toBeUndefined();
  });

  test("credsPath accepts an absolute path", () => {
    const parsed = NatsConfigSchema.parse({
      url: "nats://localhost:4222",
      credsPath: "/etc/cortex/cortex.creds",
    });
    expect(parsed.credsPath).toBe("/etc/cortex/cortex.creds");
  });

  test("credsPath accepts a ~/ path (expansion happens in loader, not schema)", () => {
    const parsed = NatsConfigSchema.parse({
      url: "nats://localhost:4222",
      credsPath: "~/.config/nats/cortex.creds",
    });
    expect(parsed.credsPath).toBe("~/.config/nats/cortex.creds");
  });

  test("credsPath rejects non-string", () => {
    expect(() => NatsConfigSchema.parse({
      url: "nats://localhost:4222",
      credsPath: 42,
    })).toThrow();
  });
});

